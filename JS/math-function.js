// conver into positive
console.log(Math.abs(-10));

// convert into upper number
console.log(Math.ceil(10.2));
// convert into lower number
console.log(Math.floor(10.8));
// convert upper if greater than or equal to .5 otherwise lower
console.log(Math.round(13.5));


// remove the decimal part
console.log(Math.trunc(10.3432424));


// return power
console.log(Math.pow(2,5));


// return max value
console.log(Math.max(12,434,657,8684,534));
// return min value
console.log(Math.min(12,434,-657,8684,534));

// return random value bw 0 to 1
console.log(Math.random());

// return fixed decimal number
let a = 34343.121343;
console.log(a.toFixed(2))

// return square root
console.log(Math.sqrt(16));
// return cube root
console.log(Math.cbrt(27));

